type ExerciseInput = Omit<Exercise, "name" | "targetSets" | "restSec"> & {
  displayName: string;
  targetSets?: number;
  restSec?: number;
};

const kgMetrics: MetricType[] = ["weightKg", "reps", "sets", "completed"];
const timeMetrics: MetricType[] = ["durationSec", "quality1to5", "completed"];
const skillMetrics: MetricType[] = ["durationSec", "quality1to5", "completed"];

function exercise(input: ExerciseInput): Exercise {
  return {
    ...input,
    name: input.displayName,
    referenceId: input.referenceId ?? input.id,
    targetSets: input.targetSets ?? 1,
    restSec: input.restSec ?? 0,
    referenceSearchQuery:
      input.referenceSearchQuery ?? `${input.displayName} execução correta exercício`,
  };
}

function strength(input: ExerciseInput): Exercise {
  return exercise({
    kind: "strength",
    priority: "support",
    metricSchema: kgMetrics,
    progressionModel: "double_progression",
    active: true,
    phaseAvailability: "phase_1",
    ...input,
  });
}

function timed(input: ExerciseInput): Exercise {
  return exercise({
    kind: input.kind ?? "timed",
    priority: input.priority ?? "support",
    metricSchema: input.metricSchema ?? timeMetrics,
    progressionModel: input.progressionModel ?? "time_progression",
    active: input.active ?? true,
    phaseAvailability: input.phaseAvailability ?? "phase_1",
    ...input,
  });
}

function skill(input: ExerciseInput): Exercise {
  return exercise({
    kind: input.kind ?? "skill",
    priority: input.priority ?? "technical",
    metricSchema: input.metricSchema ?? skillMetrics,
    progressionModel: input.progressionModel ?? "quality_progression",
    active: input.active ?? true,
    phaseAvailability: input.phaseAvailability ?? "phase_1",
    ...input,
  });
}

function block(
  id: string,
  name: string,
  type: WorkoutBlock["type"],
  items: Exercise[],
  options: Pick<WorkoutBlock, "blockMode" | "required"> = {},
): WorkoutBlock {
  return {
    id,
    name,
    type,
    blockMode: options.blockMode ?? inferBlockMode(type),
    required: options.required ?? isRequiredBlock(type),
    items,
  };
}

function inferBlockMode(type: WorkoutBlock["type"]): NonNullable<WorkoutBlock["blockMode"]> {
  if (type === "strength" || type === "hypertrophy" || type === "main") {
    return "sets";
  }
  if (type === "rounds") {
    return "rounds";
  }
  if (type === "test") {
    return "test";
  }
  if (type === "technical" || type === "skill" || type === "review") {
    return "checklist";
  }
  if (type === "cooldown" || type === "warmup" || type === "mobility" || type === "base_body") {
    return "checklist";
  }
  return "checklist";
}

function isRequiredBlock(type: WorkoutBlock["type"]): boolean {
  return type === "strength" || type === "hypertrophy" || type === "main" || type === "rounds";
}

function workoutBlocks(workout: Workout): string[] {
  return workout.workoutBlocks?.map((item) => item.name) ?? workout.blocks ?? [];
}

